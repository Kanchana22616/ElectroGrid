package com.jersy.bean;

public class enquiryAddBean {

	private String enquiryID, email, name, contact, enquiryType, enquiryDetails;

	public String getEnquiryID() {
		return enquiryID;
	}

	public void setEnquiryID(String enquiryID) {
		this.enquiryID = enquiryID;
	}
	
	
	
	
	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
	
	
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	


	public String getContact() {
		return contact;
	}

	public void setContact(String contact) {
		this.contact = contact;
	}
	
	

	public String getEnquiryType() {
		return enquiryType;
	}

	public void setEnquiryType(String enquiryType) {
		this.enquiryType = enquiryType;
	}

	
	
	
	public String getEnquiryDetails() {
		return enquiryDetails;
	}

	public void setEnquiryDetails(String enquiryDetails) {
		this.enquiryDetails = enquiryDetails;
	}
}
